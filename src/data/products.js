export function getCategoryName(id) {
  return CATEGORIES.find((c) => c.id === id)?.name || id;
}

export function formatPrice(price, unit) {
  const unitLabels = {
    sqft: '/sqft',
    gal: '/加仑',
    set: '/套',
    pc: '/件',
  };
  return `$${price.toFixed(2)}${unitLabels[unit] || ''}`;
}
